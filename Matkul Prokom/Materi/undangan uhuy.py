#program string menggantikan nama pada undangan

tulisan1 = "kepada yth: "
tulisan2 = "nama yang diundang: Justin"
tulisan3 = "alamat kota Surakarta"

print("===================")
#kop surat pertama
print(tulisan1)
print(tulisan2)
print(tulisan3)
